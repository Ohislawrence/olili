HOW TO CREATE A COURSE:

1. On your dashboard, click on New Course
2. Enter what can be the title of what you will like to learn
3. Select an exam board if it has one, if not skip this.
4. Select a subject
5. Select your Target Level, the level you will like to be at the end of the course.
6. Select a Weekly Study Hours
7. Select a Target Completion Date
8. You can choose to enter a Description and Learning Objectives if you want to
9. Sit back and learn!

